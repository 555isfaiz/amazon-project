public class Photo {
    private String ID;
    private String Time;
    private String Date;
    private String IP;
    private String Name;
    private String URL;

    public String getID()
    {
        return this.ID;
    }
    public void setID(String ID)
    {
        this.ID = ID;
    }
    
    public String getTime()
    {
        return this.Time;
    }
    public void setTime(String Time)
    {
        this.Time = Time;
    }
    
    public String getDate()
    {
        return this.Date;
    }
    public void setDate(String Date)
    {
        this.Date = Date;
    }
    
    public String getIP()
    {
        return this.IP;
    }
    public void setIP(String IP)
    {
        this.IP = IP;
    }
    
    public String getName()
    {
        return this.Name;
    }
    public void setName(String Name)
    {
        this.Name = Name;
    }
    
    public String getURL()
    {
        return this.URL;
    }
    public void setURL(String URL)
    {
        this.URL = URL;
    }
}
